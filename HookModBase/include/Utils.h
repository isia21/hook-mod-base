#pragma once

/// <summary>
/// Вспомогательные утилиты для логирования, вывода отладочной информации и диалоговых окон.
/// </summary>
namespace Utils
{
    /// <summary>
    /// Форматирует строку произвольной длины и выводит ее пользователю в виде модального окна (MessageBox).
    /// Использует динамическое выделение памяти для безопасной работы с логами любого размера.
    /// </summary>
    /// <param name="format">Строка форматирования (совместимая с printf).</param>
    /// <param name="...">Вариативные аргументы для подстановки.</param>
    /// <returns>Код возврата MessageBox.</returns>
    int Message(const char* format, ...);


    /// <summary>
    /// Форматирует строку произвольной длины и отправляет ее в отладчик (OutputDebugString).
    /// Гарантирует корректный перенос строки (\n) и защищает поток вывода от перемешивания (thread-safety).
    /// </summary>
    /// <param name="format">Строка форматирования (совместимая с printf).</param>
    /// <param name="...">Вариативные аргументы для подстановки.</param>
    void ODS(const char* format, ...);
}