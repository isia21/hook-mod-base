#pragma once

/// <summary>
/// Вспомогательные утилиты для логирования, вывода отладочной информации и диалоговых окон.
/// </summary>
namespace Utils
{
    /// <summary>
    /// Форматирует строку и выводит ее пользователю в виде модального окна сообщений (MessageBox).
    /// Имя функции-вызывающего объекта автоматически подставляется в заголовок окна (через макрос __PRETTY_FUNCTION__).
    /// </summary>
    /// <param name="format">Строка форматирования (совместимая со спецификацией printf).</param>
    /// <param name="...">Вариативные аргументы для подстановки в строку формата.</param>
    /// <returns>Результат нажатия кнопки пользователем (код возврата функции MessageBox, например, IDOK).</returns>
    int Message(const char* format, ...);

    /// <summary>
    /// Форматирует строку и отправляет ее в отладочный поток операционной системы Windows (OutputDebugString).
    /// Результат вывода можно перехватить во вкладке "Вывод" (Output) среды Visual Studio или в программе DebugView.
    /// Автоматически добавляет символ перехода на новую строку (\n) в конце вывода.
    /// </summary>
    /// <param name="format">Строка форматирования (совместимая со спецификацией printf).</param>
    /// <param name="...">Вариативные аргументы для подстановки в строку формата.</param>
    void ODS(const char* format, ...);
}